public class CombinationOfTwoLetters {

	public static void main(String[] args) {

		
		for(char i = 'a'; i <= 'e'; i ++) {
			for (char j = 'a'; j<= 'e'; j++) {
				System.out.print(i+""+j);
				System.out.println();
			}
			System.out.println("*****");
		}
		
		
		
		
		
		
		
		
		
		
		
	}

}
