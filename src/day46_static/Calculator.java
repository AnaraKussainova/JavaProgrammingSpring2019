package day46_static;

public class Calculator {
	
	
	public static double plus(double n1, double n2) {
		double result = n1 + n2;
		return  result;
	}
	
	public static double minus (double n1, double n2) {
		double result = n1 - n2;
		return  result;
	}
	
	public static double multiply(double n1, double n2) {
		double result = n1 * n2;
		return  result;
	}
}
